public class Ejercicio04 {
    public static void main(String[] args) {
        double tasaCambio = 0.2470; // Tasa de cambio de PEN a EUR

        double soles = 100; // Cantidad en soles que se desea convertir
        double euros = soles * tasaCambio;
        System.out.println(soles + " soles equivale a " + euros + " euros.");
    }
}
    

